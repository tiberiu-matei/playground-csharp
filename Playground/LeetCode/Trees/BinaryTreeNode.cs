namespace Playground.LeetCode.Trees
{
    public class BinaryTreeNode
    {
        public int val;
        public BinaryTreeNode left;
        public BinaryTreeNode right;

        public BinaryTreeNode(int val = 0, BinaryTreeNode left = null, BinaryTreeNode right = null)
        {
            this.val = val;
            this.left = left;
            this.right = right;
        }
    }
}
